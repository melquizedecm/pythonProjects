def suma(num1,num2):
    num1=float(num1)
    num2=float(num2)
    return num1+num2

def resta(num1,num2):
    num1 = float(num1)
    num2 = float(num2)
    return num1 - num2

def media(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    return (num1 + num2)/2


a=5
